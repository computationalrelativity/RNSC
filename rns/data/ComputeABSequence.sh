#!/bin/bash
# ######################################################################
# BASH SCRIPT TO COMPUTE EQUILIBRIUM SEQUENCES OF UNIFORMLY ROTATING NS
# ######################################################################
# Uses RNS version 1 is used
# Polytropic dimensionless units are used
#

# ######################################################################
# CHOOSE EXE
# ######################################################################

EXE=rns.v1.1d/rns

# ######################################################################
# COMPUTE KEPLERIAN SEQUENCE AND NON-ROTATING SEQUENCES
# ######################################################################

$EXE -t kepler -q poly -N 1 -e 0.001 -l 0.01 -n 50 -p 2 -d 0 > KeplerSeqPolyN1.dat
$EXE -t static -q poly -N 1 -e 0.001 -l 0.01 -n 50 -p 2 -d 0 > StaticSeqPolyN1.dat

# ######################################################################
# COMPUTE SEQUENCE AU 
# ######################################################################

Aec=("1.4440e-01" "1.3000e-01" "1.1870e-01" "1.0740e-01" "9.6100e-02" "8.6300e-02")
Arr=("1.0000e+00" "9.1900e-01" "8.5200e-01" "7.8000e-01" "6.9800e-01" "5.7500e-01")

numA=${#Aec}

$EXE -t model -q poly -N 1 -e ${Aec[${1}]} -r ${Arr[${1}]} -d 0 -p 2 > ASeqPolyN1.dat

for((i=1;i<$numA;i++)); do
    #echo ${Aec[${i}]} ${Arr[${i}]}
    $EXE -t model -q poly -N 1 -e ${Aec[${i}]} -r ${Arr[${i}]} -d 0 -p 2 >> ASeqPolyN1.dat
done

# ######################################################################
# COMPUTE SEQUENCE BU
# ######################################################################

Bec=("1.4440e-01" "1.4440e-01" "1.4440e-01" "1.4440e-01" "1.4440e-01" "1.4440e-01" "1.4440e-01" "1.4440e-01" "1.4440e-01" "1.4440e-01")
Brr=("1.0000e+00" "9.5000e-01" "9.0000e-01" "8.5000e-01" "8.0000e-01" "7.5000e-01" "7.0000e-01" "6.5000e-01" "6.0000e-01" "5.8000e-01")
numB=${#Bec}

$EXE -t model -q poly -N 1 -e ${Bec[${1}]} -r ${Brr[${1}]} -d 0 -p 2 > BSeqPolyN1.dat

for((i=1;i<$numB;i++)); do
    #echo ${Bec[${i}]} ${Brr[${i}]}
    $EXE -t model -q poly -N 1 -e ${Bec[${i}]} -r ${Brr[${i}]} -d 0 -p 2 >> BSeqPolyN1.dat
done

exit 0

